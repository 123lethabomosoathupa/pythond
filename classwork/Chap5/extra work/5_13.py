# 5-12. To-Do List with Boolean Checks

# Create a list of completed tasks
completed_tasks = ['homework', 'laundry', 'dishes']

# Check if certain tasks are completed
if 'homework' in completed_tasks:
    print("You finished your homework!")

if 'laundry' in completed_tasks:
    print("You finished the laundry!")

if 'dishes' in completed_tasks:
    print("You washed the dishes!")

if 'grocery shopping' in completed_tasks:
    print("You went grocery shopping!")

if 'exercise' in completed_tasks:
    print("You completed your exercise!")
